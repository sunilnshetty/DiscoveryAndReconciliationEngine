package com.lte.discovery.reconciliation.adapter.dto;


public class NE {

	String neFdn;
	String neName;
	String neType;

	public String getNeFdn() {
		return neFdn;
	}

	public void setNeFdn(String neFdn) {
		this.neFdn = neFdn;
	}

	public String getNeName() {
		return neName;
	}

	public void setNeName(String neName) {
		this.neName = neName;
	}

	public String getNeType() {
		return neType;
	}

	public void setNeType(String neType) {
		this.neType = neType;
	}

}
